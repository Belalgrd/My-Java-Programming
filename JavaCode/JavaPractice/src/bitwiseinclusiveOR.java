
public class bitwiseinclusiveOR {

	public static void main(String[] args) {
		int a = 5;  // In binary: 0101
		int b = 3;  // In binary: 0011

		// Using bitwise inclusive OR operator (|)
		int orResult = a | b;   // Result: 0111 (7 in decimal)
		System.out.println("Bitwise Inclusive OR of a and b: " + orResult);

	}

}
